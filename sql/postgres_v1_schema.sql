CREATE TABLE IF NOT EXISTS sys_dept (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  parent_id BIGINT NOT NULL DEFAULT 0,
  ancestors VARCHAR(500) NOT NULL DEFAULT '',
  dept_name VARCHAR(64) NOT NULL,
  order_num INT NOT NULL DEFAULT 0,
  leader VARCHAR(64),
  phone VARCHAR(20),
  email VARCHAR(100),
  status SMALLINT NOT NULL DEFAULT 1,
  remark VARCHAR(500),
  created_by BIGINT,
  created_at TIMESTAMP(3) WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  updated_by BIGINT,
  updated_at TIMESTAMP(3) WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  is_deleted SMALLINT NOT NULL DEFAULT 0,
  CONSTRAINT uk_sys_dept_parent_name UNIQUE (parent_id, dept_name)
);
CREATE INDEX IF NOT EXISTS idx_sys_dept_parent_status ON sys_dept (parent_id, status);

